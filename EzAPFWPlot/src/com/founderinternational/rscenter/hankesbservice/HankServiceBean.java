package com.founderinternational.rscenter.hankesbservice;

public class HankServiceBean {
	private String ESBPort;
	private String ESBContextPath;
	private String FWurl;
	private String servicename;

	public String getESBPort() {
		return ESBPort;
	}
	public void setESBPort(String eSBPort) {
		ESBPort = eSBPort;
	}
	public String getESBContextPath() {
		return ESBContextPath;
	}
	public void setESBContextPath(String eSBContextPath) {
		ESBContextPath = eSBContextPath;
	}
	public String getFWurl() {
		return FWurl;
	}
	public void setFWurl(String fWurl) {
		FWurl = fWurl;
	}
	public String getServicename() {
		return servicename;
	}
	public void setServicename(String servicename) {
		this.servicename = servicename;
	}
	
       
}
